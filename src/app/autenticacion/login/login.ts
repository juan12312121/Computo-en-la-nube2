import { CommonModule } from '@angular/common';
import { Component } from '@angular/core';
import { FormsModule } from '@angular/forms';
import { Router } from '@angular/router';
import { AutenticacionService, LoginRequest } from '../../core/servicios/autenticacion/autenticacion';

@Component({
  selector: 'app-login',
  standalone: true,
  imports: [CommonModule, FormsModule],
  templateUrl: './login.html',
  styleUrl: './login.css'
})
export class Login {
  emailOrUsername: string = '';
  password: string = '';
  showPassword: boolean = false;
  
  // Estados de la UI
  isLoading: boolean = false;
  errorMessage: string = '';
  showError: boolean = false;

  constructor(
    private router: Router,
    private authService: AutenticacionService
  ) {
    // Si ya est√° autenticado, redirigir a principal
    if (this.authService.isAuthenticated()) {
      this.router.navigate(['/principal']);
    }
  }

  togglePasswordVisibility(): void {
    this.showPassword = !this.showPassword;
  }

  onSubmit(): void {
    // Resetear mensajes de error
    this.errorMessage = '';
    this.showError = false;

    // Validaciones b√°sicas
    if (!this.emailOrUsername.trim()) {
      this.mostrarError('Por favor ingresa tu correo o nombre de usuario');
      return;
    }

    if (!this.password) {
      this.mostrarError('Por favor ingresa tu contrase√±a');
      return;
    }

    if (this.password.length < 6) {
      this.mostrarError('La contrase√±a debe tener al menos 6 caracteres');
      return;
    }

    // Preparar datos para login
    const loginData: LoginRequest = {
      email: this.emailOrUsername.trim(),
      contrasena: this.password
    };

    // Iniciar proceso de login
    this.isLoading = true;
    console.log('üîê Iniciando sesi√≥n con:', loginData);
    console.log('üì¶ Datos enviados al backend:', JSON.stringify(loginData));

    this.authService.login(loginData).subscribe({
      next: (response) => {
        console.log('‚úÖ Login exitoso:', response);
        
        if (response.success && response.data) {
          console.log('üë§ Usuario autenticado:', response.data.usuario);
          console.log('üîë Token recibido:', response.data.token);
          
          // Mostrar mensaje de √©xito
          this.mostrarExito('¬°Bienvenido de vuelta!');
          
          // Redirigir a la p√°gina principal despu√©s de un breve delay
          setTimeout(() => {
            this.router.navigate(['/principal']);
          }, 500);
        } else {
          this.mostrarError(response.mensaje || 'Error en el inicio de sesi√≥n');
        }
        
        this.isLoading = false;
      },
      error: (error) => {
        console.error('‚ùå Error en login:', error);
        this.isLoading = false;
        
        // Manejar diferentes tipos de errores
        if (error.status === 401) {
          this.mostrarError('Credenciales incorrectas. Verifica tu correo/usuario y contrase√±a.');
        } else if (error.status === 404) {
          this.mostrarError('Usuario no encontrado. ¬øNecesitas registrarte?');
        } else if (error.status === 0) {
          this.mostrarError('No se pudo conectar con el servidor. Verifica tu conexi√≥n.');
        } else if (error.error?.mensaje) {
          this.mostrarError(error.error.mensaje);
        } else {
          this.mostrarError('Error al iniciar sesi√≥n. Por favor intenta de nuevo.');
        }
      }
    });
  }

  irARegistro(): void {
    this.router.navigate(['/registro']);
  }

  // M√©todo para mostrar errores
  private mostrarError(mensaje: string): void {
    this.errorMessage = mensaje;
    this.showError = true;
    
    // Auto-ocultar despu√©s de 5 segundos
    setTimeout(() => {
      this.showError = false;
    }, 5000);
  }

  // M√©todo para mostrar mensajes de √©xito
  private mostrarExito(mensaje: string): void {
    this.errorMessage = mensaje;
    this.showError = true;
    
    // Auto-ocultar despu√©s de 2 segundos
    setTimeout(() => {
      this.showError = false;
    }, 2000);
  }

  // M√©todo para cerrar mensaje manualmente
  cerrarMensaje(): void {
    this.showError = false;
  }
}