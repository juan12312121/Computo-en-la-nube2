import { CommonModule } from '@angular/common';
import { Component, ViewEncapsulation } from '@angular/core';
import { FormsModule } from '@angular/forms';
import { Router } from '@angular/router';
import { AutenticacionService } from '../../core/servicios/autenticacion/autenticacion';

@Component({
  selector: 'app-registro',
  standalone: true,
  imports: [CommonModule, FormsModule],
  templateUrl: './registro.html',
  styleUrl: './registro.css',
  encapsulation: ViewEncapsulation.None
})
export class Registro {
  username = '';
  nombreCompleto = '';
  email = '';
  password = '';
  mostrarPassword = false;
  
  // Estados de la UI
  cargando = false;
  error = '';
  mensajeExito = '';

  constructor(
    private router: Router,
    private authService: AutenticacionService
  ) {
    console.log('üöÄ Componente Registro inicializado');
  }

  togglePasswordVisibility() {
    this.mostrarPassword = !this.mostrarPassword;
    console.log('Toggle password visibility:', this.mostrarPassword ? 'Visible' : 'Oculta');
  }

  onSubmit() {
    console.log('üìù Iniciando proceso de registro...');
    console.log('üìã Datos del formulario:', {
      username: this.username,
      nombreCompleto: this.nombreCompleto,
      email: this.email,
      passwordLength: this.password.length
    });

    // Limpiar mensajes previos
    this.error = '';
    this.mensajeExito = '';
    console.log('üßπ Mensajes de error/√©xito limpiados');

    // Validaciones b√°sicas
    if (!this.username || !this.nombreCompleto || !this.email || !this.password) {
      this.error = 'Por favor completa todos los campos';
      console.error('‚ùå Validaci√≥n fallida: Campos vac√≠os');
      console.log('   - Username:', this.username ? '‚úì' : '‚úó');
      console.log('   - Nombre completo:', this.nombreCompleto ? '‚úì' : '‚úó');
      console.log('   - Email:', this.email ? '‚úì' : '‚úó');
      console.log('   - Password:', this.password ? '‚úì' : '‚úó');
      return;
    }

    if (this.password.length < 6) {
      this.error = 'La contrase√±a debe tener al menos 6 caracteres';
      console.error('‚ùå Validaci√≥n fallida: Contrase√±a muy corta');
      console.log(`   - Longitud actual: ${this.password.length} caracteres`);
      console.log('   - Longitud m√≠nima: 6 caracteres');
      return;
    }

    if (!this.validarEmail(this.email)) {
      this.error = 'Por favor ingresa un correo electr√≥nico v√°lido';
      console.error('‚ùå Validaci√≥n fallida: Email inv√°lido');
      console.log(`   - Email ingresado: ${this.email}`);
      return;
    }

    console.log('‚úÖ Todas las validaciones pasaron');

    // Mostrar loading
    this.cargando = true;
    console.log('‚è≥ Estado de carga activado');

    // Preparar datos para el backend
    const datosRegistro = {
      nombre_usuario: this.username,
      nombre_completo: this.nombreCompleto,
      email: this.email,
      contrasena: this.password
    };

    console.log('üì¶ Datos preparados para enviar al backend:', {
      ...datosRegistro,
      contrasena: '***oculta***'
    });

    // Llamar al servicio de autenticaci√≥n
    console.log('üåê Enviando petici√≥n al servidor...');
    this.authService.registro(datosRegistro).subscribe({
      next: (response) => {
        console.log('‚úÖ Registro exitoso - Respuesta del servidor:', response);
        this.cargando = false;
        console.log('‚è≥ Estado de carga desactivado');
        
        this.mensajeExito = '¬°Cuenta creada exitosamente! Redirigiendo...';
        console.log('üéâ Mensaje de √©xito mostrado');
        
        // Redirigir al home o dashboard despu√©s de 1.5 segundos
        console.log('‚è±Ô∏è Iniciando redirecci√≥n en 1.5 segundos...');
        setTimeout(() => {
          console.log('üîÄ Redirigiendo a /principal');
          this.router.navigate(['/principal']);
        }, 1500);
      },
      error: (error) => {
        console.error('‚ùå Error en registro:', error);
        console.log('üìä Detalles del error:');
        console.log('   - Status:', error.status);
        console.log('   - StatusText:', error.statusText);
        console.log('   - Error completo:', error.error);
        
        this.cargando = false;
        console.log('‚è≥ Estado de carga desactivado');
        
        // Mostrar mensaje de error espec√≠fico del backend
        if (error.error?.mensaje) {
          this.error = error.error.mensaje;
          console.log('üí¨ Mensaje de error del backend:', error.error.mensaje);
        } else if (error.status === 0) {
          this.error = 'No se pudo conectar con el servidor. Verifica tu conexi√≥n.';
          console.error('üîå Error de conexi√≥n: No se puede alcanzar el servidor');
        } else {
          this.error = 'Error al crear la cuenta. Por favor intenta de nuevo.';
          console.error(`‚ö†Ô∏è Error gen√©rico con status ${error.status}`);
        }
        
        console.log('üö® Error mostrado al usuario:', this.error);
      }
    });
  }

  // Validar formato de email
  private validarEmail(email: string): boolean {
    const regex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    const esValido = regex.test(email);
    console.log('üìß Validaci√≥n de email:', {
      email: email,
      esValido: esValido,
      regex: regex.toString()
    });
    return esValido;
  }

  irALogin() {
    console.log('üîÄ Navegando a la p√°gina de login');
    this.router.navigate(['/login']);
  }
}